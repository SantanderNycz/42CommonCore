#include "libft.h"

char	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*pb;

	i = 0;
	pb = (unsigned char *)b;
	while (i < len)
	{
		*pb = (unsigned char)c;
		pb++;
		i++;
	}
	return (b);
}
